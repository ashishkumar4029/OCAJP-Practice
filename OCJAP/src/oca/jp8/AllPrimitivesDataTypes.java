package oca.jp8;

import java.util.Vector;

public class AllPrimitivesDataTypes {
    
    public static void main(String[] args) {
        //integers
        byte bytePrim=100;
        short shortPrim=10000;
        int intPrim=100000;
        long longPrim=10000000;
        //fractional
        float floatPrim=200;
        double doublePrim=200000;
        //binary
        boolean boolPrim=false;
        
        System.out.println("byte "+bytePrim+"; short "+shortPrim+"; int "+intPrim+";  long "+longPrim+";");
        System.out.println("float "+floatPrim+";   double "+doublePrim+";");
        System.out.println("boolean "+boolPrim);
    }

}
